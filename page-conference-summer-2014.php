<p>August 27-31st</p>
<h2>Welcome to the ownCloud Contributor Conference!</h2>
<p>This is where you find out what is going on in ownCloud, learn how to develop apps, be inspired by fellow ownClouders and improve your ownCloud!</p>
<p>The coding starts on Wednesday 27th and continues until Sunday the 31st. Saturday features a conference track with keynotes, lightning talks and workshops, providing insight in ownCloud and a chance to learn new skills.</p>
<h2>Program</h2>
<p>a <strong>Keynote</strong> by XYZ opens the program on Saturday.</p>
<p>In our lightning track, we then share what is going on in the ownCloud apps like Read Later, Calendar, News, Notes, Music, Documents, Activity and many more.</p>
<p>Afteroon workshops include:</p>
<ul>
	<li>Learn to write your first ownCloud App</li>
	<li>Secure your PHP application</li>
	<li>Learn how to write code tests</li>
	<li>Connect your JS app to ownCloud</li>
	<li>Add the ownCloud Share API to your application</li>
</ul>
