<h2>Welcome to the ownCloud Contributor Conference!</h2>
<p>This is where you find out what is going on in ownCloud, learn how to change ownCloud, be inspired by fellow ownClouders and build your ownCloud!</p>
<p>With millions of users, ownCloud is THE open source file sync and sharing solution and that is because of its awesome contributors. For the second time, they will gather at the TU Berlin for 5 days of writing code and sharing beer, knowledge and inspiration.</p>
<h2>Where and when?</h2>
<p>Our conference takes place at the TU Berlin, XXXXXX (address).</p>
<p>The coding starts on Wednesday 27th of August and continues until the 31st. Saturday the 30th features a conference track with keynotes, lightning talks and workshops, providing insight in ownCloud and a chance to learn new skills.</p>
