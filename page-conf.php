<h1>Hackathon</h1>
<h3>August 26-31</h3>
<p>For the second time, the awesome community behind ownCloud will get together at the TU Berlin for 5 days of writing code and sharing beer, knowledge and inspiration. We will get work done:</p>
<ul>
	<li>Coding (PHP, JS, C++)</li>
	<li>Design &amp; Front-end</li>
	<li>Testing</li>
	<li>Translation &amp; Documentation</li>
	<li>Marketing &amp; Promo</li>
</ul>
</div>
<div class="span6">
<h1>Techtalks &amp; Workshops</h1>
<h3>August 30</h3>
<p>The Conference Day on Saturday the 30th provides a chance to learn and improve your coding skills on and around ownCloud.</p>

<p>Join us for keynotes, lightning talks and workshops:</p>
<ul>
	<li>Learn to write your first ownCloud App</li>
	<li>Secure your PHP application</li>
	<li>Learn how to write code tests</li>
	<li>Connect your JS app to ownCloud</li>
	<li>Add the ownCloud Share API to your application</li>
</ul>
