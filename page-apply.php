    <h1>Apply to be a listed Provider</h1>
</div>
<div class="row providers">
    <div class="span12">
        <p>To apply to be listed on the <a href="/providers">ownCloud.org/providers</a> webpage as an open source provider, please send an email to <strong>providers (at) owncloud (dot) org</strong> with the following details:</p>
        <ul>
            <li>Your company name</li>
            <li>The website link to your ownCloud offering</li>
            <li>A 290 x 70 pixel logo banner as an attachment (.gif, .jpg or .png)</li>
            <li>Your target clients (businesses, consumers, a mix etc)</li>
            <li>Headquarter location</li>
            <li>Whether you offer free ownCloud accounts</li>        
        </ul>
        <p>If you are interested in becoming an enterprise provider with a support contract from <a target="_blank" href="https://owncloud.com">ownCloud Inc</a> you can find more information <a target="_blank" href="https://owncloud.com/products/service-provider">here</a>.</p>
